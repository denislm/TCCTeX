    public boolean testarOdt(File arquivo){
        //pega nome do arquivo
    String nomeOdt = arquivo.getName().toString();
        //Verifica se nome termina com .odt
        if(nomeOdt.endsWith(".odt")){
            //Verifica se arquivo existe
             if(arquivo.exists()){
                 //se Existir retorna true
                 return true;
             }else{
                 //Se não existir: desmarca terminei caixa está parte
                getjCheckBox1().setSelected(false);
            JOptionPane.showMessageDialog(null, "Erro: Arquivo não encontrado!","ERRO",JOptionPane.ERROR_MESSAGE);
            return false;
            }
}else{  //Arquivo não tem extensão ".odt", erro
        JOptionPane.showMessageDialog(null, "Erro: Só são permitidos arquivos .odt","ERRO,",JOptionPane.ERROR_MESSAGE);
        jTCapitulo.setText("Selecione o Arquivo e clique em Adicionar");
        return false;
        }        
}
