    private void jTermTextuaisActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jTermTextuaisActionPerformed
File introducao, conclusao;
        
        
introducao = new File(getjTIntroducao().getText());
conclusao = new File(getjTConclusao().getText());
//Pega arquivo de introdução pelo campo de texto

if(getjCheckBox1().isSelected()){
        t.setIntroducao(introducao);

//Pega todos capitulos da tabela
        capitulos.clear();
        for(int i=0; i < getDtm().getRowCount(); i++){
        capitulos.add(new File(getDtm().getValueAt(i, 2)+""));
        }
        t.setCapitulos(capitulos);

        //Pega arquivo de conclusão pelo campo de texto
        t.setConclusao(conclusao);
        try {
            if(introducao.exists() && conclusao.exists()&&capitulos.get(0).exists()){
            t.converterTextuais();
            this.setVisible(false);
            TelaPrincipal.tela.getImagem3().setIcon(new javax.swing.ImageIcon(getClass().getResource("/br/metodista/TCCtex/telas/imagens/certo.png")));
            
            
            String textuaisTex="";
            for( int i=0; i<ArquivosGerados().size(); i++){
                textuaisTex = textuaisTex+"\\input{"+ArquivosGerados().get(i)+"}\n";
            }
            Conversor c = new Conversor();
            textuaisTex = "\\textual \n"+textuaisTex; 
            File textual = new File("LaTex\\textual.tex");
                   c.escreverArquivo(textuaisTex, textual);
            
            
            TelaPrincipal.tela.setVisible(true);
            
            }else{
                JOptionPane.showMessageDialog(null, "Erro, insira todos arquivos obrigatórios,\n\n"
                        + "     - Introdução;\n"
                        + "     - Um Capítulo pelo menos;\n"
                        + "     - Conclusão.","ERRO",JOptionPane.ERROR_MESSAGE);
                getjCheckBox1().setSelected(false);
            }         
        } catch (IOException ex) {
            Logger.getLogger(TelaTextuais.class.getName()).log(Level.SEVERE, null, ex);
        }
        
}else{
//apagar arquivos tex se gerados (não implementado)
    TelaPrincipal.tela.getImagem3().setIcon(new javax.swing.ImageIcon(getClass().getResource("/br/metodista/TCCtex/telas/imagens/errado.png")));
    }
    }//GEN-LAST:event_jTermTextuaisActionPerformed

