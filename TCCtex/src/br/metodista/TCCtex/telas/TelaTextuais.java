    private File selectArquivo(String mensagem){
        
        //Seleção apenas de arquivos Opendocument (extensão: .odt)
        buscaOdt.setFileFilter(new FileNameExtensionFilter("Opendocument \".odt\"", "odt"));
        buscaOdt.setAcceptAllFileFilterUsed(false);
        //Texto que aparece na seleção do arquivo.
        buscaOdt.setDialogTitle(mensagem);
        /* Abre a caixa de seleção de arquivo e verifica e cancela se 
         * botão cancel for pressionado*/
        if(buscaOdt.showOpenDialog(this)!= JFileChooser.APPROVE_OPTION){
        return null;
        }
        //Envia caminho completo do arquivo
        return buscaOdt.getSelectedFile().getAbsoluteFile();
        
    }
    
