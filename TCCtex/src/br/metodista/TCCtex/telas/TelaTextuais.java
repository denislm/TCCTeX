    private void jButton12ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton12ActionPerformed
  int selectedRow = jTable1.getSelectedRow();
  try{
  if(selectedRow >= 0 && selectedRow < (dtm.getRowCount()-1)){
  String l11 = dtm.getValueAt(selectedRow, 1).toString();
  String l12 = dtm.getValueAt(selectedRow, 2).toString();
  String l21 = dtm.getValueAt((selectedRow+1), 1).toString();
  String l22 = dtm.getValueAt((selectedRow+1), 2).toString();
  //Coloca o valor da linha de baixo na linha de cima
  dtm.setValueAt(l21, selectedRow, 1);
  dtm.setValueAt(l22, selectedRow, 2);
  //Coloca o valor da linha de cima na linha de baixo
    dtm.setValueAt(l11, (selectedRow+1), 1);
    dtm.setValueAt(l12, (selectedRow+1), 2);
    jTable1.setRowSelectionInterval((selectedRow+1),1);
    
  }
  }catch (ArrayIndexOutOfBoundsException e){
  System.out.println("Erro: "+e);
  }
    }//GEN-LAST:event_jButton12ActionPerformed

