    public LinkedList<String> ArquivosGerados(){
        LinkedList<String> arquivosGerados = new LinkedList<>();
        String a,b,c,d,e,f;
        a = (new File(getjTResumo().getText())).getName().replace( ".odt", "" );
        b = (new File(getjTAbstract().getText())).getName().replace( ".odt", "" );
        d = (new File(getjTDedicatoria().getText())).getName().replace( ".odt", "" );
        e = (new File(getjTAgradecimentos().getText())).getName().replace( ".odt", "" );
        f = (new File(getjTEpigrafe().getText())).getName().replace( ".odt", "" );
        arquivosGerados.add(a);arquivosGerados.add(b);
        arquivosGerados.add(d);arquivosGerados.add(e);arquivosGerados.add(f);
        return arquivosGerados;
    }

