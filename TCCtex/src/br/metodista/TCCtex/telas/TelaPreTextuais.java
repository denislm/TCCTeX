    private void jCheckBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jCheckBox1ActionPerformed
//Passar conversão de todos arquivos para cá ou não?
   if(  getjCheckBox1().isSelected()){    
       if( new File(getjTResumo().getText()).exists()  
      &&   new File(getjTAbstract().getText()).exists() ){
      try {    
          
        caminho = new File(getjTResumo().getText());
            p.setResumoPtBr(caminho);
        
        caminho = new File(getjTAbstract().getText());
            p.setResumoEnUs(caminho);
            
        
        caminho = new File(getjTDedicatoria().getText());
            p.setDedicatoria(caminho);
 
        caminho = new File(getjTAgradecimentos().getText());
            p.setAgradecimento(caminho);
            
        caminho = new File(getjTEpigrafe().getText());
            p.setEpigrafe(caminho);
            
        p.converterPre();
        
        this.setVisible(false);
        TelaPrincipal.tela.getImagem2().setIcon(new javax.swing.ImageIcon(getClass().getResource("/br/metodista/TCCtex/telas/imagens/certo.png")));
        TelaPrincipal.tela.setVisible(true);
      } catch (NullPointerException e) {
            
        }  catch (IOException ex) {
               Logger.getLogger(TelaPreTextuais.class.getName()).log(Level.SEVERE, null, ex);
           }
             
       }else{
                getjCheckBox1().setSelected(false);
            JOptionPane.showMessageDialog(null, "Erro, você não inseriu todos arquivos obrigatórios! (*)","ERRO",JOptionPane.ERROR_MESSAGE);
       }
   }else{
       //apagar arquivos tex se gerados (não implementado)
       TelaPrincipal.tela.getImagem2().setIcon(new javax.swing.ImageIcon(getClass().getResource("/br/metodista/TCCtex/telas/imagens/errado.png")));
   }
    }//GEN-LAST:event_jCheckBox1ActionPerformed

