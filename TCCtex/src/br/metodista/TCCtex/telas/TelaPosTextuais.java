    private File selectArquivo(String mensagem, String tipo){
        if(tipo.equals("odt")){
        //Seleção apenas de arquivos Opendocument (extensão: .odt)
        buscaExt.setFileFilter(new FileNameExtensionFilter("Opendocument \".odt\"", "odt"));
        }else{
        //Seleção apenas de arquivos PDF)
        buscaExt.setFileFilter(new FileNameExtensionFilter("Opendocument \".pdf\"", "pdf"));
        }
        buscaExt.setAcceptAllFileFilterUsed(false);
        //Texto que aparece na seleção do arquivo.
        buscaExt.setDialogTitle(mensagem);
        /* Abre a caixa de seleção de arquivo e verifica e cancela se 
         * botão cancel for pressionado*/
        if(buscaExt.showOpenDialog(this)!= JFileChooser.APPROVE_OPTION){
        return null;
        }
        //Envia caminho completo do arquivo
        return buscaExt.getSelectedFile().getAbsoluteFile();
        
    }
