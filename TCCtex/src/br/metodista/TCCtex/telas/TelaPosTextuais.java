    private void jCheckBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jCheckBox1ActionPerformed
if(jCheckBox1.isSelected()){
        PosTextuais p = new PosTextuais();
        
        p.setReferencias(new File(jTReferencias.getText()));
        p.setObrasConsultadas(new File(jTObras.getText()));
        p.setRrbq(new File(jTRRBQ.getText()));
        p.setCronograma(new File(jTCronograma.getText()));
        LinkedList<File> atas = new LinkedList();
        for(int i=0; i < getDtm().getRowCount(); i++){
        atas.add(new File(getDtm().getValueAt(i, 1)+""));
        }
        p.setAtas(atas);
        if(p.getReferencias().exists() && p.getRrbq().exists()){
        try {
            p.converterPosTextuais();
            p.setAtas(new LinkedList());
            this.setVisible(false);
            TelaPrincipal.tela.getImagem4().setIcon(new javax.swing.ImageIcon(getClass().getResource("/br/metodista/TCCtex/telas/imagens/certo.png")));
            TelaPrincipal.tela.setVisible(true);
        } catch (IOException ex) {
            Logger.getLogger(TelaPosTextuais.class.getName()).log(Level.SEVERE, null, ex);
        }
        }else{
            JOptionPane.showMessageDialog(null, "Erro, insira todos arquivos obrigatórios,\n\n"
                        + "     - Referências;\n"
                        + "     - RRBQ.","ERRO",JOptionPane.ERROR_MESSAGE);
                jCheckBox1.setSelected(false);        
        }
}else{

TelaPrincipal.tela.getImagem4().setIcon(new javax.swing.ImageIcon(getClass().getResource("/br/metodista/TCCtex/telas/imagens/errado.png")));

}
 
    }//GEN-LAST:event_jCheckBox1ActionPerformed

