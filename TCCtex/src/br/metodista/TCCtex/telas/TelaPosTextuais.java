    private void jButton18ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton18ActionPerformed
  int selectedRow = jTable1.getSelectedRow();
  try{
  if(selectedRow >= 0 && selectedRow < (dtm.getRowCount()-1)){
  String l1 = dtm.getValueAt(selectedRow, 1).toString();
  String l2 = dtm.getValueAt((selectedRow+1), 1).toString();
  //Coloca o valor da linha de baixo na linha de cima
  dtm.setValueAt(l2, selectedRow, 1);
  //Coloca o valor da linha de cima na linha de baixo
    dtm.setValueAt(l1, (selectedRow+1), 1);
    jTable1.setRowSelectionInterval((selectedRow+1),1);
    
  }
  }catch (ArrayIndexOutOfBoundsException e){
  System.out.println("Erro: "+e);
  }
    }//GEN-LAST:event_jButton18ActionPerformed

